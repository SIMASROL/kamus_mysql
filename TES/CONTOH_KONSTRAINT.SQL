1. PRIMARY KEY.
CREATE TABLE mahasiswa (
  NIM INT(12) AUTO_INCREMENT PRIMARY KEY,
  nama VARCHAR NOT NULL
);

CREATE TABLE KRS (
  kode_MK VARCHAR(10),
  NIM INT(12),
  semester INT,
  PRIMARY KEY (kode_MK, NIM)
);

2. NOT NULL.
CREATE TABLE mahasiswa (
  NIM INT(12) AUTO_INCREMENT PRIMARY KEY,
  nama VARCHAR NOT NULL,
  status VARCHAR NOT NULL DEFAULT 'SINGLE'
);

3. UNIQUE.
CREATE TABLE mahasiswa (
  NIM INT(12) AUTO_INCREMENT PRIMARY KEY,
  nama VARCHAR NOT NULL,
  email VARCHAR NOT NULL UNIQUE
);

CREATE TABLE mahasiswa (
  NIM INT(12) AUTO_INCREMENT PRIMARY KEY,
  nama VARCHAR NOT NULL,
  username VARCHAR NOT NULL,
  pacar VARCHAR,
  UNIQUE (username, pacar)
);

4. CHECK.
CREATE TABLE mahasiswa (
  NIM INT(12) AUTO_INCREMENT PRIMARY KEY,
  nama VARCHAR NOT NULL,
  usia INT CHECK (usia >= 18)
);

CREATE TABLE mahasiswa (
  NIM INT(12) AUTO_INCREMENT PRIMARY KEY,
  nama VARCHAR PRIMARY KEY,
  gender CHAR(1) CHECK (gender IN ('L', 'P'))
);

CREATE TABLE mahasiswa (
  NIM INT(12) AUTO_INCREMENT PRIMARY KEY,
  nama VARCHAR NOT NULL,
  IPK DECIMAL(3,2) CHECK (IPK BETWEEN 0.00 AND 4.00)
);

CREATE TABLE pengguna (
  ID INT AUTO_INCREMENT PRIMARY KEY,
  usename VARCHAR NOT NULL UNIQUE,
  password VARCHAR(255) CHECK (LENGTH(password) >=8)
);

